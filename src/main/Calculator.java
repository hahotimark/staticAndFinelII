package main;

public class Calculator {

    public static void sum(int a, int b){

        int res = a + b;

        System.out.println("result: " + res);
    }

    public static void mult(int a, int b){

        int res = a * b;

        System.out.println("result: " + res);
    }

    public static void sub(int a, int b){

        int res = a - b;

        System.out.println("result: " + res);
    }

    public static void div(int a, int b){

        int res = a / b;

        System.out.println("result: " + res);
    }
}
